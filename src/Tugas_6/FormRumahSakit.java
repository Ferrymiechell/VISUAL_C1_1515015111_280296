    private void BTNsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNsimpanActionPerformed
        // TODO add your handling code here:
        int pilih = JOptionPane.showConfirmDialog(this, "Yakin ingin simpan data ?","Confirm Simpan",JOptionPane.YES_OPTION);
        if(pilih==JOptionPane.YES_OPTION){
        String jk;
        String nama_pasien = TFnama.getText();
        String tl = TFtl.getText();
        String usia = TFusia.getText();
        
        if(RBlaki.isSelected()){
           jk = RBlaki.getText();
        }
        else if(RBperempuan.isSelected()){
            jk = RBperempuan.getText();
        }
        else{
            JOptionPane.showMessageDialog(null, "Pilih jenis kelamin dulu !!!",
            "Peringatan",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        
        String diagnosa = TFdiagnosa.getText();
        String dokter = CMBdokter.getSelectedItem().toString();
        String ruangan = CMBruangan.getSelectedItem().toString();
        String tgl_masuk = TFtglmasuk.getText();
        TambahData(nama_pasien,tl,usia,jk,diagnosa,dokter,ruangan,tgl_masuk);
        bersih();
        }
    }//GEN-LAST:event_BTNsimpanActionPerformed
