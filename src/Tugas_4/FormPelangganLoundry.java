    private void TFteleponKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TFteleponKeyPressed
        // TODO add your handling code here:
        TFtelepon.addKeyListener(new KeyAdapter(){
            public void keyTyped(KeyEvent e){
                char karakter = e.getKeyChar();
                if (!((karakter >='0') && (karakter <='9') || (karakter == '\b'))){
                    e.consume();            
                }
            }
        });
    }//GEN-LAST:event_TFteleponKeyPressed

    private void TFberatKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TFberatKeyPressed
        // TODO add your handling code here:
        TFberat.addKeyListener(new KeyAdapter(){
            public void keyTyped(KeyEvent e){
                char karakter = e.getKeyChar();
                if ((((karakter < '0') || (karakter > '9')) && (karakter != '\b')) && (karakter != '.')){
                    e.consume();              
                }
            }
        });
    }//GEN-LAST:event_TFberatKeyPressed

