    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        // TODO add your handling code here:
        String nama,telepon,paket;
        float total_harga,total_diskon,berat,reguler=11500;
        float diskon = jSlider1.getValue();
        berat = Float.parseFloat(TFberat.getText());
        nama = TFnama.getText();
        telepon = TFtelepon.getText();
        
        if(RBreguler.isSelected()){
           paket = RBreguler.getText();
           total_diskon = (reguler * berat) *(diskon/100);
           total_harga = (reguler*berat) - total_diskon;
        }
        else if(RBexpress.isSelected()){
            paket = RBexpress.getText();
           total_diskon = (float) ((reguler*1.5 * berat) *(diskon/100));
           total_harga = (float) ((reguler*1.5 * berat) - total_diskon);
        }
        else{
            JOptionPane.showMessageDialog(null, "Pilih paket pencucian dulu !!!",
            "Peringatan",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
            TFtotalharga.setText("Rp. "+String.valueOf(total_harga));
            
            TAnota.setText("================================"+
                            "\nNama\t: "+nama+
                            "\nNo Telepon\t: "+telepon+
                            "\nBerat\t: "+berat+" Kg"+
                            "\nPaket\t: "+paket+
                            "\nDiskon\t: Rp "+total_diskon+
                            "\nTotal Bayar\t: Rp "+total_harga+
                            "\n=============================="+
                            "\n"+
                            "\n---------------- TERIMA KASIH ----------------");
    }//GEN-LAST:event_btnProsesActionPerformed

